#include <p3rt/forward.h>
#include <p3rt/dispatch.h>

STATIC_P3LOCK_UNLOCKED(p3rt_req_lock);

void p3rt_forward(void *request, size_t reqlen, void *result, size_t reslen)
{
	p3_lock(&p3rt_req_lock);
	p3rt_dispatch(request, result);
	p3_unlock(&p3rt_req_lock);
}

